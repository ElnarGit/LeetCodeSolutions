package solved;

public class MoveZeroes {

    // Метод для перемещения всех нулей в конец массива, сохраняя порядок ненулевых элементов
    public void moveZeroes(int [] nums){
        // Указатель на позицию, куда нужно поместить следующий ненулевой элемент
        int index = 0;

        // Первый проход: перемещаем все ненулевые элементы в начало массива
        for(int i = 0; i < nums.length; i++){
            if(nums[i] != 0){               // Если текущий элемент не равен 0
                nums[index++] = nums[i];    // Перемещаем элемент в позицию index и увеличиваем index
            }
        }

        // Второй проход: заполняем оставшиеся элементы нулями
        for(int i = index; i < nums.length; i++){
            nums[i] = 0; // Все позиции после index заполняются нулями
        }
    }
}
